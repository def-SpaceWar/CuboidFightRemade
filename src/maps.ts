export function loadMap(mapNum: number, players: Player[]) {
  const mapPlatforms = [];
  // @ts-ignore: can't find type
  const map = maps[`map${mapNum}`];

  for (let y = 0; y < map.length; y++) {
    for (let x = 0; x < map[y].length; x++) {
      switch (map[y][x]) {
        case "1":
          players[0].x = UNIT_SIZE * x;
          players[0].y = UNIT_SIZE * y;
          players[0].respawnPoint = [UNIT_SIZE * x, UNIT_SIZE * y];
          break;
        case "2":
          players[1].x = UNIT_SIZE * x;
          players[1].y = UNIT_SIZE * y;
          players[1].respawnPoint = [UNIT_SIZE * x, UNIT_SIZE * y];
          break;
        case "3":
          players[2].x = UNIT_SIZE * x;
          players[2].y = UNIT_SIZE * y;
          players[2].respawnPoint = [UNIT_SIZE * x, UNIT_SIZE * y];
          break;
        case "4":
          players[3].x = UNIT_SIZE * x;
          players[3].y = UNIT_SIZE * y;
          players[3].respawnPoint = [UNIT_SIZE * x, UNIT_SIZE * y];
          break;
        case "g":
          const color = `#${Math.floor(Math.random() * 3) + 5}f${Math.floor(Math.random() * 3) + 2}`;

          mapPlatforms.push(
            new Platform(
              UNIT_SIZE * x,
              UNIT_SIZE * y,
              UNIT_SIZE,
              UNIT_SIZE, {
                top: color,
                bottom: "#320"
              },
              "grass"
            ),
          )

          break;
        case "s":
          let val: string = (Math.floor(Math.random() * 8) + 5).toString();

          let intVal = parseInt(val);
          if (intVal > 9) {
            if (intVal == 10) val = "A";
            if (intVal == 11) val = "B";
            if (intVal == 12) val = "C";
            if (intVal == 13) val = "D";
          }

          mapPlatforms.push(
            new Platform(
              UNIT_SIZE * x,
              UNIT_SIZE * y,
              UNIT_SIZE,
              UNIT_SIZE, {
                top: `#B${val}B${val}B${val}`,
                bottom: "#666"
              },
              "stone",
              true
            ),
          )

          break;
        default:
          break;
      }
    }
  }

  // @ts-ignore
  return [mapPlatforms, maps[`bg${mapNum}`]];
}
